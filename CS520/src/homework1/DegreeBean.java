package homework1;

public class DegreeBean {
	String degree;
	String univ;
	String year;
	public DegreeBean(String degree, String univ, String year) {
		super();
		this.degree = degree;
		this.univ = univ;
		this.year = year;
	}
	public String getDegree() {
		return degree;
	}
	public String getUniv() {
		return univ;
	}
	public String getYear() {
		return year;
	}
	
}
